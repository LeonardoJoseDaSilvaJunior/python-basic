# > Opereações matemáticas (Aritiméticas)

"""
    - Soma: +
    - Subtração: -
    - Multiplicão: *
    - Divisão: /
    - Divisão inteira: //
    - Resto da divisão: %
    - potências: **
""" 

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 // 3)
print(20 % 3)
print(2 ** 3)

