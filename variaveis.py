"""
    tipos de variáveis 
    1. int: números inteiros, ou seja, números sem parte decimal:0, 5, -1, 1000
    2. float: números reais, ou seja, números com parte decimal: 1.0, -2.7, 3.14
    3. str(string): cadeias de caracteres, ou seja, dados textuais
    4. bool(boolean): valores lógicos (booleanos): true(verdadeiro) ou false(falso)
"""
idade = 26 #int
altura = 1.70 #float
nome = 'leonardo josé' #str
estudando = True #bool

print(type(idade))
print(type(altura))
print(type(nome))
print(type(estudando))






